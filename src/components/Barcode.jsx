function encodeCode128B(text) {
  // Value 104 = START B
  const START_B_VAL = 104
  const STOP_VAL    = 106

  let checksum = START_B_VAL
  const dataVals = []

  for (let i = 0; i < text.length; i++) {
    const ascii = text.charCodeAt(i)
    const val = ascii - 32   // Code128B: space=0, A=33, ...
    if (val < 0 || val > 95) continue  // skip unencodable chars
    dataVals.push(val)
    checksum += val * (i + 1)
  }

  const checkVal = checksum % 103

  // Build bar sequence
  const allPatterns = [
    START_B_PATTERN,
    ...dataVals.map(v => CODE128B_CHARS[v]),
    CODE128B_CHARS[checkVal] || CODE128B_CHARS[0],
    STOP_PATTERN,
  ]

  const bars = []
  allPatterns.forEach(pattern => {
    pattern.forEach((width, i) => {
      bars.push({ width, isBar: i % 2 === 0 })
    })
  })

  return bars
}

export default function Barcode({ value, width = 300, height = 80, fontSize = 13 }) {
  const bars = encodeCode128B(value)
  const totalUnits = bars.reduce((s, b) => s + b.width, 0)
  const unitWidth  = width / totalUnits

  let cursor = 0

  return (
    <svg
      width={width}
      height={height + 22}
      xmlns="http://www.w3.org/2000/svg"
      style={{ display: 'block' }}
    >
      <rect width={width} height={height + 22} fill="white" />
      {bars.map((b, i) => {
        const bx = cursor
        const bw = b.width * unitWidth
        cursor += bw
        if (!b.isBar) return null
        return (
          <rect
            key={i}
            x={bx}
            y={0}
            width={Math.max(bw, 0.8)}
            height={height}
            fill="black"
          />
        )
      })}
      <text
        x={width / 2}
        y={height + 17}
        textAnchor="middle"
        fontSize={fontSize}
        fontFamily="Courier New, monospace"
        fontWeight="bold"
        fill="#000"
        letterSpacing="3"
      >
        {value}
      </text>
    </svg>
  )
}
